#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

int	main(int argc, char	**argv)
{
	int	i;

	i = 1;
	while (argc < i - 1)
	{
		ft_swap(argv[i], argv[i + 1]);
		i++;
	}
	return (0);
}
